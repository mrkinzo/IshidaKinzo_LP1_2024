

public class new_File_Kish {
 
  public static void main(String[] args) {
    
    int x=5;
    int y=5;

    for (int i = 0; i < x; i++) {
        for (int j = 0; j < y; j++) {
            if (i>=j) {
              System.out.print('X');  
            }else{
                System.out.print('O');
            }
        }
        
    }
  } 
}
